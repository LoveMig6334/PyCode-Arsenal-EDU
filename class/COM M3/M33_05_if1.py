# นาย ธรรศ บุนนาค เลขที่ 5 ชั้น 3/3

aqi = int(input("กรอกค่าดัชนีค่าคุณภาพอากาศ (AQI): "))

if aqi > 301:
    print("อันตรายต่อสุขภาพ")
elif aqi >= 201:
    print("มีผลกระทบต่อสุขภาพมาก ")
elif aqi >= 151:
    print("มีผลกระทบต่อสุขภาพ ")
elif aqi >= 101:
    print("เริ่มมีผลกระทบต่อสุขภาพ ")
elif aqi >= 51:
    print("คุณภาพอากาศปานกลาง ")
else:
    print("คุณภาพอากาศดี ")
